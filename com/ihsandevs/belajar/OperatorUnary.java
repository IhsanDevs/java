package com.ihsandevs.belajar;

public class OperatorUnary {
    public static void main(String[] args) {
        /*
            Operator unary adalah operasi
            yang dilakukan pada satu variabel.
        */
        int b = 10;
        b++;

        System.out.println(b);
        for (int i = 1; i <= 10; i++) {
            System.out.printf("Angka %d \n", i);
        }
        
    }
}
